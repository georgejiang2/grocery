    private void deleteProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProductButtonActionPerformed
        // TODO add your handling code here:
        deleteProductTextArea.setText("");
        String studentID = deleteProductComboBox.getSelectedItem().toString().split(" ", 3)[2];
        StudentEntry student = StudentQueries.getStudent(studentID);
        String name = student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID();
        StudentQueries.dropStudent(studentID);
        deleteProductTextArea.append(name + " has been dropped from the list of students. \n\n");
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String s : semesters) {
            ArrayList <ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(s, studentID);
            if (schedule.size() > 0) {
                deleteProductTextArea.append("For Semester " + s + "\n");
                for (ScheduleEntry se : schedule) {
                    if (se.getStatus().equals("W")) {
                        ScheduleQueries.dropStudentScheduleByCourse(s, studentID, se.getCourseCode());
                        deleteProductTextArea.append(name + " has been dropped from the waitlist for " + se.getCourseCode() + "\n");
                    } else {
                        ScheduleQueries.dropStudentScheduleByCourse(s, studentID, se.getCourseCode());
                        deleteProductTextArea.append(name + " has been dropped from " + se.getCourseCode() + "\n");
                        ArrayList <ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByCourse(s, se.getCourseCode());
                        if (waitlist.size() > 0) {
                            ScheduleQueries.updateScheduleEntry(s, waitlist.get(0));
                            StudentEntry waitlistedStudent = StudentQueries.getStudent(waitlist.get(0).getStudentID());
                            String waitlistedName = waitlistedStudent.getLastName() + ", " + waitlistedStudent.getFirstName() + " " + waitlistedStudent.getStudentID();
                            deleteProductTextArea.append(waitlistedName + " has been scheduled into " + se.getCourseCode() + "\n");
                        }
                    }
                }
                deleteProductTextArea.append("\n");
            }

        }
        rebuildComboBoxes();
    }//GEN-LAST:event_deleteProductButtonActionPerformed

    
    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            project = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            project = "Course Scheduler Spring 2023";

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(project.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

