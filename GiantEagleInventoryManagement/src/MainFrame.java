    private void addCustomerProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerProductButtonActionPerformed
        // TODO add your handling code here:
        String semester = currentSemesterComboBox.getSelectedItem().toString();
        String courseCode = addProductComboBox.getSelectedItem().toString();
        String studentName = currentCustomerTextField.getText();
        String[] names = studentName.split(" ", 3);
        String studentID = names[2];
        studentName = names[0] + " " + names[1];

        String status = "";
        if (ScheduleQueries.getScheduledStudentCount(semester,courseCode) >= CourseQueries.getCourseSeats(semester, courseCode)) {
            status = "W";
        } else {
            status = "S";
        }
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        ScheduleQueries.addScheduleEntry(new ScheduleEntry(semester, studentID, courseCode, status, currentTimestamp));

        if (status == "W") {    
            addCustomerProductStatusLabel.setText(studentName + " has been waitlisted for the class.");    
        } else {
            addCustomerProductStatusLabel.setText(studentName + " has been scheduled for the class.");
        }
        
        rebuildComboBoxes();
    }//GEN-LAST:event_addCustomerProductButtonActionPerformed

