    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
        ArrayList<StudentEntry> se = StudentQueries.getAllStudents();
        ArrayList<String> students = new ArrayList<String>();
        for (StudentEntry s : se) {
            students.add(s.getLastName()+", "+s.getFirstName() + " " + s.getStudentID());
        }
        if (students.size() > 0) {
            currentCustomerTextField.setText(students.get(0));
        } else {
            currentCustomerTextField.setText("None, add a student.");
        }
    }
    public void rebuildComboBoxes() {
        // student combo box
        if (currentCustomerTextField.equals("None, add a student.")) {
            ArrayList<StudentEntry> se = StudentQueries.getAllStudents();
            currentCustomerTextField.setText(se.getLastName() + ", "+ se.getFirstName() + " " + se.getStudentID());
        }
        ArrayList<StudentEntry> se = StudentQueries.getAllStudents();
        ArrayList<String> students = new ArrayList<String>();
        for (StudentEntry s : se) {
            students.add(s.getLastName()+", "+s.getFirstName() + " " + s.getStudentID());
        }
        currentCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        deleteProductComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        
        //course combo box
        ArrayList<String> courses = new ArrayList<String>();
        if (SemesterQueries.getSemesterList().size() > 0) {
            courses = CourseQueries.getAllCourseCodes(currentSemesterComboBox.getSelectedItem().toString());
            
        }
        ArrayList<ScheduleEntry> schedule = new ArrayList<ScheduleEntry>();
        ArrayList<String> studentSchedule = new ArrayList<String>();
        if (SemesterQueries.getSemesterList().size() > 0 && students.size() > 0) {
            schedule = ScheduleQueries.getScheduleByStudent(currentSemesterLabel.getText(), currentCustomerTextField.getText().split(" ", 3)[2]);
            for (ScheduleEntry s : schedule) {
                studentSchedule.add(s.getCourseCode());
            }
        }

        deleteItemComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentSchedule.toArray()));
        listSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        addProductComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        deleteCustomerComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
    }
    
    

