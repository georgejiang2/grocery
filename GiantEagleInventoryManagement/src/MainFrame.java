    private void deleteItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteItemButtonActionPerformed
        // TODO add your handling code here:
        dropCourseTextArea.setText("");
        String semester = currentSemesterLabel.getText();
        String studentID = currentCustomerTextField.getText().split(" ", 3)[2];
        String courseCode = deleteItemComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> w = ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode);
        
        ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, courseCode);
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode);
        if (w.size() != waitlisted.size()) {
            // person was on waitlist
            dropCourseTextArea.append(currentCustomerTextField.getText() + " has been dropped from the waitlist for " + courseCode + "\n");
        } else {
            // schedule new person
            dropCourseTextArea.append(currentCustomerTextField.getText() + " has been dropped from " + courseCode + "\n");
            if (waitlisted.size() > 0) {
                ScheduleQueries.updateScheduleEntry(semester,waitlisted.get(0));
                StudentEntry waitlistedStudent = StudentQueries.getStudent(waitlisted.get(0).getStudentID());
                String name = waitlistedStudent.getLastName() + ", " + waitlistedStudent.getFirstName() + " " + waitlistedStudent.getStudentID();
                dropCourseTextArea.append(name + " has been scheduled into " + courseCode);
            }
        }

        
        
    }//GEN-LAST:event_deleteItemButtonActionPerformed

